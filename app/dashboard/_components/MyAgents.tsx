"use client";
import { UserDetailContext } from "@/context/UserDetailContext";
import { api } from "@/convex/_generated/api";
import { Agent } from "@/types/AgentTypes";
import { useConvex } from "convex/react";
import { GitBranchPlusIcon } from "lucide-react";
import moment from "moment";
import Link from "next/link";
import { useContext, useEffect, useState } from "react";

function MyAgents() {
  const { userDetail } = useContext(UserDetailContext);
  const [agentList, setAgentList] = useState<Agent[]>([]);
  const convex = useConvex();

  useEffect(() => {
    userDetail && getUserAgents();
  }, [userDetail]);

  console.log({ userDetail });

  const getUserAgents = async () => {
    const result = await convex.query(api.agent.GetUserAgents, {
      userId: userDetail?._id,
    });

    setAgentList(result);
  };
  return (
    <div className="w-full mt-5">
      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-5">
        {agentList.map((agent, index) => (
          <Link
            href={`/agent-builder/${agent?.agentId}`}
            key={index}
            className="p-3 border rounded-2xl shadow"
          >
            <GitBranchPlusIcon className="bg-yellow-100 p-2 rounded-sm h-8 w-8 " />
            <h2 className="mt-3">{agent.name}</h2>
            <h2 className="text-gray-400 mt-2 text-sm">
              {moment(agent._creationTime).fromNow()}
            </h2>
          </Link>
        ))}
      </div>
    </div>
  );
}
export default MyAgents;
